package Chapter02;

public class Variable {

	public static void main(String[] args) {
		// variable 1.name 2.data 3.price
		int  myAge;
		myAge = 10;
		System.out.println(myAge);
		
		int value; // 변수의 선언
		value = 10;
		int result = value + 10; // 변수의 선언+타변수 가져오기
		// '=' 오른쪽의 값을 왼쪽으로 넣어준다
		/*
		 * 변수 사용을 위해서는 반드시 변수의 초기화가 필요하다
		 */
		System.out.println(result);
		

	}

}