package Chapter02;

public class floatdoubbkle {

	public static void main(String[] args) {
	/*
	 * float: 4byte, 소수점 7자리까지
	 * double: 8byte, 소수점 15자리까지
	 * 
	 */
		
	float var1 = 3.14f;
	double var2 = 3.14;
	
	System.out.println(var1);
		
	// 지수(exponential) 사용하기 e
	double var3 = 3e6;//3*10^6;
	double var4 = 2e-3;//2*10^-3
	
	}

}
