2021-0821-03)오라클 데이터 타입
    - 오라클에서 제공하는 데이터 타입은 문자열, 숫자, 날짜, 2진자료 타입이 있음
    - 문자형, 논리형은 없음
    - 오라클은 ''에 묶이면 무조건 문자열이다
    - 오라클은 숫자를 나타낼수 있는 것은 NUMBER뿐이다
    - 오라클에서는 다양한 문자열이 있다. CHAR(고정) VARCHAR2(가변) CLOB(아주긴 문자)
    - 영화 음악 등 디지털 포맷으로 바꿔야된다 이게 바로 2진 자료
1. 문자열 자료형 
    - ''로 묶인 자료(대문자와 소문자를 완벽하게 구분한다)
    - CHAR(고정), VARCHAR(가변), VARCHAR2(국제표준코드), NVARCHAR(국제표준코드), LONG(서비스개선종료), RAW(서비스개선종료), 
      CLOB(대용량 문자열), NCLOB(국제표준코드 대용량-UTF-8등) 등이 제공됨
      --VARCHAR2는 오라클에서만 사용한다, 고정길이는 CHAR하나이다
    1)CHAR(거의 안쓰인다, 보통은 기본키를 설정할때 많이 사용)
    - 고정길이 문자열 저장
    - 최대 2000 BYTE까지 처리 가능
    - 데이터보다 확보된 기억장소가 크면 오른쪽에 남는 공간 만큼 공백이 채워진다.
    - 한글 한 글자는 3BYTE를 차지함
    --완성형 한글인 한글자마다 코드값 부여 조합형 한글은 초성 중성 종성과 같이 각각마다 1바이트 부여 사용자가 결합해서 사용
    - 기본키나 길이가 고정된 컬럼(주민번호 등)에 사용
    (사용형식)
    컬럼명 CHAR(크기 [BYTE|CHAR])
    -'크기': 정수로 1~2000 값 사용
    -'BYTE|CHAR': 생략되면 'BYTE'로 간주되며, '크기'로 기술된 값이
     BYTE 수인지 글자수 (CHAR)인지 결정하며 2000BYTE를 초과할 수 없음
     EX)한글은 최대 몇글자를 저장할 수 있나?
     --기억장소가 부족하면 오류가 발생한다.
     --문자는 왼쪽 정렬
     --숫자는 NUMBER 날짜는 DATE 문자열은 VARCHAR2
사용예) CREATE TABLE TEMP 01(
