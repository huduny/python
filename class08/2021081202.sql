    SELECT *|[DISTINCT] 컬럼명 [AS 별칭][,]
        컬럼명 [AS 별칭][,]
            -
            -
        컬럼명 [AS 별칭]
    FROM 테이블명
    [WHERE 조건]
    [GROUP BY 컬럼명[,컬럼명,...]]
    [HAVING 조건]
    [ORDER BY 컬럼명|컬럼인덱스 [ASC|DESC][컬럼명|컬럼인덱스 [ASC|DESC],...]];
    --SELECT는 매우 자주 사용된다. SQL의 80%이상 사용
    --SELECT FROM WEHRE 절이라고 하고 뼈대가 된다.
    --SELECT 컬럼 WHERE 행 담당
    --DISTINCT 중복배제라는 뜻이다.
    --EX)주소지만, 대표만 나타내는 것이 DISTICT이다. 나머지 컬럼에 대한 중복도 배제된다.
    --별칭은 출력할때 컬럼에 제목을 표시해주고 또다른 목적은 쿼리를 할때 안쪽의 쿼리 결과를 바깥쪽에서 '참조'할때 이름으로서 사용된다.
    --오라클은 문자를 ''에표시 ""는 별칭을 쓸때, 반드시 써야될때는 특수문자(공백, 예약어 등)을 쓸때는 써야된다.
    --FROM이 안보인다는 에러는 별칭이나 콤마를 확인해 본다.
    --실행되는 순서 1.FROM 2.WHERE 3.SELECT SELECT별칭은 WHERE절에 실행이 안된다.
    --SUM AVERAGE COUNT MINUM MAXIMUM 그룹함수 5가지(GROUP BY, HAVING 절을 사용할려면 하나 이상은 무조건 사용)
    --ORDER BY 테이블에 있는 컬럼명이던지 SELECT에 있는 컬럼을 사용할려면 기술되어진 순서대로(1번부터 센다) 컬럼이름OR인덱스(기술한 순서)를 쓰면 된다.
    --ASC작 큰 DESC큰 작은거, 생략가능 생략하면 ASC가 기본, 컬럼값이 똑같으면 인덱스 순으로 정렬된다. EX)나이 ASC/ 성별 ASC
    --GROUP BY 컬럼명: 그룹을 묶을 때 기준이 되는 컬럼명이다. 부서나 교실등
    --조건이 부여되어 질때는 HAVING을 사용해야 된다. EX)회원별 각 달 구매수량 합계를 구했으면, 구매횟수를 구했다면=COUNT함수를 사용 5회이상, SUM은 그룹
    --함수와 같이 그룹함수에 조건이 부여될때는 WHERE절을 쓰면 안되고 HAVING절을 써야된다***
