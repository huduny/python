2)VARCHAR2
- 가변길이 문자열 저장
- 최대 4000BYTE 처리 가능
- 확보된 기억공간에서 데이터 저장에 사용된 공간을 제외한 공간은 운영체제에게 반납
- VARCHAR와 같은 기능 
- 가장 널리 사용되는 데이터 타입
- NVARCHAR는 국제 표준코드(UTF-8,UTF-16)로 변화 저장
(사용법)
    컬럼명 VARCHAR2(크기 [BYTE|CHAR])
3)LONG
- 가변길이 문자열 저장
- 최대 2GB꺼자 저장 가능
- 한 테이블에 하나의 컬럼만 LONG타입으로 사용할 수 있음 --가장 큰 단점
- 기능개선이 종료되었음(CLOB로 대체)--CLOB는 사용되는 컬럼의 개수에 제한이 없다., LONG과 CLOB는 길이를 지정하지 않는다
- SELECT문의 SELECT절, UPDATE문의 SET절,INSERT문의 VALUES절에서 사용가능
(사용법)
    컬럼명 LONG;
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
